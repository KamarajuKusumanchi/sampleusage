#!/usr/bin/env python3

counter = 10    # An integer
miles = 100.0   # A floating point
name = "John"   # A string

print(counter)
print(miles)
print(name)

# demonstrate multiple assignment
# Assign same value to several variables
a = b = c = 1
print(a)
print(b)
print(c)

# assign different values to different variables
a, b, c = 1, 2.0, "Smith"
print(a, b, c)

